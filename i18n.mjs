export function normalizeLanguage(language) {
  return language === "zh" ? "zh" : "en";
}

export function translate(language, key, params = {}) {
  const dictionary = translations[normalizeLanguage(language)];
  const template = dictionary[key] ?? translations.en[key] ?? key;
  return String(template).replace(/\{(\w+)\}/g, (_, name) => params[name] ?? "");
}

export function translationKeys(language) {
  return Object.keys(translations[normalizeLanguage(language)]);
}
